function validarFormulario() {
    var valor = true;
    if ($("#nombre_dia").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_dia").focus();
    }
    return valor;
}
function limpiarFormulario(){
    $("#id_dia").val("0");
    $("#nombre_dia").val("");
}

