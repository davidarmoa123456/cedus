function validarFormulario() {
    var valor = true;
    if ($("#nombre_sexo").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_sexo").focus();
    }
    return valor;
}
function limpiarFormulario(){
    $("#id_sexo").val("0");
    $("#nombre_sexo").val("");
}
