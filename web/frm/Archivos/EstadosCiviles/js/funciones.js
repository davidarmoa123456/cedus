function tecla(event) {

    num = event.keyCode;

    if (num == 13) {
        $("#buscar").load("buscar.html");
        $("#buscar").fadeIn("slow");
        $("#panelPrograma").fadeOut("slow");
    }


}
function salir(event) {

    num = event.keyCode;

    if (num == 27) {
        $("#buscar").fadeOut("slow");
        $("#panelPrograma").fadeIn("slow");
        
    }


}
function reload(event) {

    num = event.keyCode;

    if (num == 127) {
         
        location.reload(true);
    }


}