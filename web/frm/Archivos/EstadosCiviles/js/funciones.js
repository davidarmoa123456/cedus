function validarFormulario() {
    var valor = true;
    if ($("#nombre_estadocivil").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_estadocivil").focus();
    }
    return valor;
}
function limpiarFormulario(){
    $("#id_estadocivil").val("0");
    $("#nombre_estadocivil").val("");
}
