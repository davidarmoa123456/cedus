function validarFormulario() {
    var valor = true;
    if ($("#inicio_hora").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#inicio_hora").focus();
    }
    return valor;
}
function limpiarFormulario(){
    $("#id_horario").val("0");
    $("#inicio_hora").val("");
    $("#fin_hora").val("");
}

function tecla(event) {

    num = event.keyCode;

    if (num == 13) {
        $("#buscar").load("buscar.html");
        $("#buscar").fadeIn("slow");
        $("#panelPrograma").fadeOut("slow");
    }


}
function salir(event) {

    num = event.keyCode;

    if (num == 27) {
        $("#buscar").fadeOut("slow");
        $("#panelPrograma").fadeIn("slow");
        
    }


}