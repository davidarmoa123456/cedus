function validarFormulario() {
    var valor = true;
    if ($("#nombre_usuario").val().trim() === "") {
        valor = false;
        $("#mensajes").html("Nombre no puede estar vacio.");
        $("#nombre_usuario").focus();
    }
    return valor;
}
function limpiarFormulario() {
    $("#id_usuario").val(0);
    $("#login_usuario").val("");
    $("#nombre_usuario").val("");
    $("#password_usuario").val("");
}
